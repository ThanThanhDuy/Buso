class User {
  String id;
  String avatarUrl;
  String userName;
  String fullName;
  String phone;
  String email;
  String roleId;

  User({
    required this.id,
    required this.avatarUrl,
    required this.userName,
    required this.fullName,
    required this.phone,
    required this.email,
    required this.roleId,
  });
}
